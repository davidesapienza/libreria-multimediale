    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Libreria libric=new Libreria();
        InfoBase o=new InfoBase();
        if(!cercapar.equals(jTextField1.getText())){
            System.err.println("entrato "+cercapar+"  "+jTextField1.getText());
            rowcerca=0;
            colcerca=-1;
            cercapar=jTextField1.getText();
            trovris=false;
        }
        
        cercaStringa();
        if(!trovatocerca && !trovris ){
            DialogErrore de=new DialogErrore(this, true, "Nessuna corrispondenza trovata");
            de.setVisible(true);
            jTextField1.setText("");
            cercapar="";
            jButton1.setEnabled(false);
        }
        else if(!trovatocerca && trovris){
            DialogStampaRicerca de=new DialogStampaRicerca(this, true, "Ricerca terminata",libric);
            de.setVisible(true);
            jTextField1.setText("");
            cercapar="";
            jButton1.setEnabled(false);
        }
        else{
            //ricavo l'elemento
            if(!t.getValueAt(rowcerca, 6).toString().isEmpty()){
                if(libric.getLunghezza()!=0 && !libric.ottieniElemento(libric.getLunghezza()-1).equals(
                                                    lib.ottieniElemento(rowcerca))){
                    o=new Ebook();
                    o=lib.ottieniElemento(rowcerca);
                    libric.aggiungiEbook((Ebook) o);
                }      
            }
            else if(!t.getValueAt(rowcerca, 5).toString().isEmpty()){
                if(libric.getLunghezza()!=0 && !libric.ottieniElemento(libric.getLunghezza()-1).equals(
                                                    lib.ottieniElemento(rowcerca))){
                    o=new BraniMusicali();
                    o=lib.ottieniElemento(rowcerca);
                    libric.aggiungiBranoMusicale((BraniMusicali) (o));
                }
                
            }
            else{
                if(libric.getLunghezza()!=0 && !libric.ottieniElemento(libric.getLunghezza()-1).equals(
                                                    lib.ottieniElemento(rowcerca))){
                    o=new Video();
                    o=lib.ottieniElemento(rowcerca);
                    libric.aggiungiVideo((Video) o);
                }
                
            }
            
            Table1.changeSelection(rowcerca, colcerca, false, false);
            System.out.println("row"+rowcerca+" col"+colcerca);
            trovatocerca=false;
            trovris=true;
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

