    private void cercaStringa(){
        while(!trovatocerca && rowcerca<lib.getLunghezza()){
            if(colcerca<6){
                colcerca++;
                System.err.println("sono incrrementato");
            }
            else{
                rowcerca++;
                colcerca=0;
            }
            
            //controllo ogni campo
            
            if(rowcerca<lib.getLunghezza() && t.getValueAt(rowcerca, colcerca).toString().contains(cercapar)){
                trovatocerca=true;
       
            }
            
                
                        
        }
        
    }
